<?php

echo "<p>99乘法表--------------------for---------------------------</p>";

for($j=1;$j<=9;$j++){
    for($i=1;$i<=$j;$i++){
        echo "{$i}*{$j}=".($i*$j)." ";
    }
    echo "<br>";
}

echo "<p>99乘法表--------------------while---------------------------</p>";

$j=1;
while($j<=9){
    $i=1;
    while($i<=$j){
         echo "{$i}*{$j}=".($i*$j)." ";
         $i++;
    }
    echo "<br>";
    $j++;
}

echo "<p>99乘法表--------------------do while---------------------------</p>";
$j=1;

do{
    $i=1;
    do{
       echo "{$i}*{$j}=".($i*$j)." ";
       $i++;
    }while($i<=$j);

    echo "<br>";

    $j++;

}while($j<=9);